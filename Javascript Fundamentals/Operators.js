// Arithmetic operators
let sum = 10 + 5;
let diff = 10 - 5;
let product = 10 * 5;
let quotient = 10 / 5;
let remainder = 10 % 5;

// Assignment operators
let x = 5;
x += 2;

// Comparison operators
console.log(5 == 5); 
console.log(5 === '5'); 

// Ternary operator
let result = (x > 10) ? "Greater" : "Lesser";
console.log(result);

// Optional chaining
let user = { profile: { name: "John" } };
console.log(user?.profile?.name); 

// Nullish coalescing
let value = null ?? 'default';
console.log(value); 
